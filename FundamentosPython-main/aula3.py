""" 
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Adriano Domingues')
print('Adriano "Domingues"')
print(1, 'Adriano "Domingues"')

# Aspas duplas
print("Adriano Domingues")
print(2, "Adriano 'Domingues'")

# Escape
print("Adriano \"Domingues\"")


# r - Espressões Regulares
print(r"Adriano \"Domingues\"")

# Exercício
print('Explícito', 'é', 'melhor " do que implícito')