# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11)
print(-11)
print(0)

#float -> Número com ponto flutuante
print(1.1 , 10.11)
print(0.0, -1.5)

# função type mostra o tipo que o Python
# inferiu ao valor
print(  type('Adriano')  )
print(  type(123)  )
print(  type(12.3), type(-11.3)  )
print(  type(12000.300000000000000)  )
print(  type(True)  )
