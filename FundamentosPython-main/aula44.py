""" 
    For + Range
    range -> range(start, stop, stop)
"""
    
numeros = range(10)
# numeros = range(5, 10)
# numeros = range(5, 10, 2)
print(numeros)

for numero in numeros:
    print(numero)
print('-------------------')
numeros1 = range(5, 10)
for numero in numeros1:
    print(numero)
print('-------------------')    
numeros2 = range(5, 10, 2)
for numero in numeros2:
    print(numero)


